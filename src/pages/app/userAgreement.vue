        methods: {
            init() {
                var urlParam = util.getUrlParam('isShowAgreeBtns');
                if(!util.isEmpty(urlParam) && urlParam=='true'){
                    this.isShowBtns = true;
                }
            },
            goBack:function () {
                var backUrl = util.getSession('versionFrom')
                if(backUrl){
                    location.href = backUrl;
                }else {
                    window.history.go(-1);
                }
            },
            notAgree(){
                if(util.isDispatchPushFromApp()){
                    document.location='webGoBack://';//返回操作
                }else {
                    this.goBack()
                }
            },
            agree(){
                if(util.isDispatchPushFromApp()){
                    document.location='signTJDP://';//执行app同意协议接口
                }else {
                    var version = util.getUrlParam('agreementVersion')
                    if(!util.isEmpty(version) && this.isShowBtns){
                        tjdServices.request({
                            method: 'agreeTJDP',
                            agreementVersion:version
                        }).then(data => {
                            this.goBack()
                        })
                    }
                }
            }
        }
    }
</script>

