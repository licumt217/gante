    import { tjdServices } from '../../assets/js/tjdServices'
    export default {
        data() {
            return {
                isShowBtns:false,
            }
        },
        mounted: function() {
            util.initAPPCommInfo();
            this.init();
//            在新的协议弹窗中不会跳到当前页来进行同意或者不同意协议的操作的,理论上本页只是个静态页,不需要逻辑操作。但是为了兼容老版本app所以还得保留逻辑操作。
